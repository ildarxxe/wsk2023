<?php
// Если запрос не является файлом (например, image.png),
// то мы перенаправляем его на наш основной серверный скрипт (server.php).
// Это позволяет обрабатывать "красивые" URL-адреса.
if (preg_match('/\.(?:png|jpg|jpeg|gif|css|js|map)$/', $_SERVER["REQUEST_URI"])) {
    return false;    // Встроенный сервер обработает статические файлы сам
} else {
    include 'server.php'; // ВСЕ остальные запросы идут сюда
}
?>